export function validateExtraFormData(data) {
  const errors = {};
  const add = (k, msg) => {
    if (!errors[k]) errors[k] = [];
    errors[k].push(msg);
  };

  for (const key of Object.keys(RULES)) {
    const rule = RULES[key];
    const node = data?.[key];

    if (!node) {
      if (rule.required) add(key, "Missing question");
      continue;
    }

    switch (rule.kind) {
      case "text": {
        const v = (node.answer ?? "").trim();
        if (rule.required && !v) add(key, "Required");
        if (rule.required && rule.min && v.length < rule.min) {
          add(key, `Must be at least ${rule.min} characters`);
        }
        break;
      }
      case "multi": {
        const arr = Array.isArray(node.answer) ? node.answer : [];
        const min = rule.min ?? 1;
        if (rule.required && arr.length < min) {
          add(key, `Select at least ${min} option${min > 1 ? "s" : ""}`);
        }
        break;
      }
      case "select": {
        const v = node.answer;
        if (rule.required && (v === undefined || v === null || v === "")) {
          add(key, "Please select a value");
        }
        break;
      }
      case "group-text": {
        const group = Array.isArray(node.answer) ? node.answer : [];
        if (rule.required && group.length === 0) add(key, "Missing items");
        group.forEach((sub, idx) => {
          const v = (sub?.answer ?? "").trim();
          if (!v) add(`${key}[${idx}]`, "Required");
          if (rule.min && v.length < rule.min) {
            add(`${key}[${idx}]`, `Must be at least ${rule.min} characters`);
          }
        });
        break;
      }
    }
  }

  return { valid: Object.keys(errors).length === 0, errors };
}
